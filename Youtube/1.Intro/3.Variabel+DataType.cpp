#include <iostream>
using namespace std;

int main() {
    // integer (int) = A whole number
    int year = 2022;
    // double (double) = Decimal number
    double damage = 2.5;
    //character (char) = 1 character
    char rank = 'S';
    // string (string) = 1 or more character
    string type = "Berserker";
    //boolean (bool) = true or false (1 or 0)
    bool attack = true;
    
    cout << "Year : " << year << endl;
    cout << "Damage : " << damage << "X" << endl;
    cout << "Rank : " << rank << endl;
    cout << "Type : " << type << endl;
    cout << "Attacker? : " << attack << endl;
}