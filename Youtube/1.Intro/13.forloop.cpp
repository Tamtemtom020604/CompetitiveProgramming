#include <iostream>
using namespace std;

int main(){
    // for loops used to make a program do a set of code for a certain amount of time
    // syntax = for (arg1;arg2;arg3)
    // arg1 = the variabel to customize
    // arg2 = a condition where it will ends (If this condition false, the program will stops)
    // arg3 = the process of how the variabel in arg1 one wil become a condition in arg2

    for( int i = 10; i >= 0; i--) {
        cout << i << endl;
    }
    
    cout << "Happy New Year!" << endl;
}